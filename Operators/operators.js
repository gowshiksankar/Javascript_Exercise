//  Arithmetic
let value1=100;
let value2=20;
console.log(value1 + value2);
console.log(value1 - value2);
console.log(value1 * value2);
console.log(value1 / value2);
console.log(value1 % value2);
console.log(value1 ** value2);
console.log(value1++);
console.log(value1--);

// Pre Increament

let a = 5;
console.log(++a); // 6 (value increased, THEN printed)
console.log(a);   // 6

//post Increament

let b = 5;
console.log(b++); // 5 (printed first, THEN increased)
console.log(b);   // 6

// Pre Increament using (-)

let c = 5;
console.log(--c); // 4
console.log(c);   // 4


//post Decreament

let d = 5;
console.log(d--); // 5
console.log(d);   // 4






